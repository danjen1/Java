public class Attend
{
    private String name;
    private String date;














    /*******************
    Getters and Setters
     *******************/
    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getDate()
    {
        return date;
    }

    public void setDate(String date)
    {
        this.date = date;
    }



}
